/**
 * AI Model Attribution Engine v2
 *
 * Architecture: evidence-weighted Bayesian scoring
 *
 *   score(model) = Σ w(pattern_i) × match(pattern_i, content) × lang_boost(model, lang)
 *
 * Each model has positive patterns (evidence FOR) and negative patterns
 * (evidence AGAINST). Negative patterns reduce the model's total score.
 *
 * Human signals accumulate independently and suppress all AI scores.
 *
 * Final confidence is normalized against the full posterior distribution,
 * not a simple softmax — models compete for the same evidence pool.
 */

export type AIModel =
  | "github-copilot"
  | "chatgpt"
  | "gemini"
  | "claude"
  | "codewhisperer"
  | "cursor"
  | "tabnine"
  | "human"
  | "unknown";

export interface AttributionResult {
  model:      AIModel;
  confidence: number;               // 0–1, normalized posterior
  signals:    string[];             // human-readable evidence list
  breakdown:  Record<AIModel, number>;
  humanEvidence: number;            // 0–1.5, raw (un-normalized) human-signal strength
}

// ── Pattern definitions ────────────────────────────────────────────────────────

interface Pattern {
  re:     RegExp;
  weight: number;    // contribution when matched
  note:   string;    // shown in signals list
}

interface ModelDef {
  model:    AIModel;
  prior:    number;       // base probability (language-agnostic)
  positive: Pattern[];    // evidence FOR this model
  negative: Pattern[];    // evidence AGAINST (reduces score)
  langBoost: Partial<Record<string, number>>; // language multipliers
}

