// ── Attribution algorithm ─────────────────────────────────────────────────────

export function attributeCode(content: string, language: string): AttributionResult {
  if (!content || content.trim().length < 50) {
    return { model:"unknown", confidence:0, signals:[], breakdown: emptyBreakdown(), humanEvidence: 0 };
  }

  const scores: Record<AIModel, number> = emptyBreakdown();
  const evidenceLog: string[] = [];

  // Score each AI model
  for (const def of MODELS) {
    let raw = def.prior;

    // Positive evidence accumulation
    for (const p of def.positive) {
      if (p.re.test(content)) {
        raw += p.weight;
        if (raw - def.prior > 0.1 && !evidenceLog.includes(p.note)) {
          evidenceLog.push(`[${def.model}] ${p.note}`);
        }
      }
    }

    // Negative evidence (penalise — scales down by the pattern's weight)
    for (const n of def.negative) {
      if (n.re.test(content)) raw *= (1 - n.weight);
    }

    // Language boost (multiply after accumulation)
    const boost = def.langBoost[language] ?? 1.0;
    scores[def.model] = Math.min(3.0, raw * boost);  // cap raw score at 3x prior
  }

  // Human signal accumulation
  let humanEvidence = 0;
  const humanNotes: string[] = [];
  for (const h of HUMAN_SIGNALS) {
    if (h.re.test(content)) {
      humanEvidence += h.weight;
      humanNotes.push(h.note);
    }
  }
  humanEvidence = Math.min(1.5, humanEvidence);
  scores.human = humanEvidence;

  // Human evidence suppresses all AI scores proportionally
  if (humanEvidence > 0) {
    const suppression = 1 - Math.min(0.75, humanEvidence * 0.5);
    for (const model of MODELS) {
      scores[model.model] *= suppression;
    }
  }

  // Normalise to a proper posterior distribution
  const total = Object.values(scores).reduce((a, b) => a + b, 0) || 1;
  const posterior: Record<AIModel, number> = {} as Record<AIModel, number>;
  for (const k of Object.keys(scores) as AIModel[]) {
    posterior[k] = scores[k] / total;
  }

  // Winner
  const entries = Object.entries(posterior) as [AIModel, number][];
  entries.sort(([, a], [, b]) => b - a);
  const [topModel, topConf] = entries[0];
  const [secondModel, secondConf] = entries[1] ?? ["unknown", 0];

  // Confidence requires winning by a margin (suppress "unknown" wins with low evidence)
  const margin = topConf - secondConf;
  const model: AIModel =
    topModel === "unknown" || topConf < 0.20 ? "unknown" :
    topModel === "human"   || margin < 0.05  ? (humanEvidence > 0.3 ? "human" : "unknown") :
    topModel;

  const confidence = Math.min(1, topConf * (1 + margin));  // boost by margin

  // Build human-readable signals for the UI
  const signals: string[] = [];
  for (const [m, p] of entries.slice(0, 3)) {
    if (p < 0.10) continue;
    const pct = Math.round(p * 100);
    if (m === "github-copilot") signals.push(`GitHub Copilot: ${pct}% — inline comment style and TypeScript patterns`);
    if (m === "chatgpt")        signals.push(`ChatGPT: ${pct}% — verbose docstrings, step-by-step comments`);
    if (m === "gemini")         signals.push(`Gemini: ${pct}% — Google-style docstrings, typing module usage`);
    if (m === "claude")         signals.push(`Claude: ${pct}% — section separators, conversational comments`);
    if (m === "codewhisperer")  signals.push(`CodeWhisperer: ${pct}% — boto3/AWS SDK patterns`);
    if (m === "cursor")         signals.push(`Cursor: ${pct}% — [AI] labels, file-context comments`);
    if (m === "tabnine")        signals.push(`Tabnine: ${pct}% — minimal comments, short completions`);
    if (m === "human")          signals.push(`Human: ${pct}% — ${humanNotes[0] ?? "personal style markers"}`);
  }
  if (model === "unknown") signals.push("Attribution unclear — mixed or insufficient signals");

  // Add top evidence from evidence log
  for (const e of evidenceLog.slice(0, 2)) signals.push(e);

  return { model, confidence, signals, breakdown: posterior, humanEvidence };
}

function emptyBreakdown(): Record<AIModel, number> {
  return { "github-copilot":0, chatgpt:0, gemini:0, claude:0, codewhisperer:0, cursor:0, tabnine:0, human:0, unknown:0 };
}

